#Visual Effect: Console Color
def red(txt): return "\033[91m"+txt+"\033[00m"

def green(txt): return "\033[92m"+txt+"\033[00m"

def yellow(txt): return "\033[93m"+txt+"\033[00m"

def purple(txt): return"\033[95m"+txt+"\033[00m"
 
def cyan(txt): return"\033[96m"+txt+"\033[00m"
 
def orange(txt): return "\033[33m"+txt+"\033[00m"

def blue(txt): return"\033[34m"+txt+"\033[00m"
 
def pink(txt): return"\033[95m"+txt+"\033[00m"

#Visual Effect: Console Style
def bold(txt): return"\033[01m"+txt+"\033[00m"

def underlined(txt): return"\033[04m"+txt+"\033[00m"